const normalizeSchoolLevel = (value) => (value === "middle" || value === "high" ? value : "");

export function getExamAnalysisPhraseOptions({ role = "", field = "", schoolLevel = "", tags = [] } = {}) {
  const normalizedLevel = normalizeSchoolLevel(schoolLevel);
  const requestedTags = Array.isArray(tags) ? tags.filter(Boolean) : [];
  return EXAM_ANALYSIS_PHRASE_LIBRARY.filter((item) => {
    if (role && item.role !== role) return false;
    if (field && item.field !== field) return false;
    if (normalizedLevel && !item.schoolLevels.includes(normalizedLevel)) return false;
    if (requestedTags.length > 0 && !requestedTags.every((tag) => item.tags.includes(tag))) return false;
    return true;
  });
}

export function getExamAnalysisPhraseById(phraseId) {
  return EXAM_ANALYSIS_PHRASE_LIBRARY.find((item) => item.id === phraseId) || null;
}

export function createExamAnalysisPhraseDraft(phraseId) {
  return getExamAnalysisPhraseById(phraseId)?.draft || "";
}
